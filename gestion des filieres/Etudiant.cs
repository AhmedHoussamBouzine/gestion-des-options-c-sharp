using System;

public class Etudiant
{
	public Etudiant(string nom , string prenom , string cne, double note)
	{
		this.Enom = nom;
		this.Eprenom = prenom;
		this.Ecne = cne;
		this.Enote = note;		
	}
   
	
	public string Enom;
	public string Eprenom;
	public string Ecne;
	public double Enote;



}
